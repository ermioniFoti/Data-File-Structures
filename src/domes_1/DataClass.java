package domes_1;

public class DataClass {

	int key;
	String data;

	public DataClass(int key, String data) {
		this.key = key;
		this.data = data;
	}

	public int getKey() {
		return key;
	}

	public void setKey(int key) {
		this.key = key;
	}

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

}
